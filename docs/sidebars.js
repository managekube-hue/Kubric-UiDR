// @ts-check

const fs = require('fs');
const path = require('path');

const DOCS_ROOT = path.join(__dirname, 'docs');

const ROOT_ORDER = [
  '01_K-CORE-01_INFRASTRUCTURE',
  '00_K-VENDOR-00_DETECTION_ASSETS',
  '02_K-XRO-02_SUPER_AGENT',
  '03_K-KAI-03_ORCHESTRATION',
  '04_K-SOC-04_SECURITY',
  '05_K-NOC-05_OPERATIONS',
  '06_K-PSA-06_BUSINESS',
  '07_K-GRC-07_COMPLIANCE',
  '11_K-MAP-11_DR_MODULE_MAPPING',
  '12_K-DEPLOY-12_TOPOLOGIES',
  '08_K-DEV-08_DEVELOPMENT',
  '09_K-API-09_API_REFERENCE',
  '10_K-ITIL-10_ITIL_MATRIX',
];

function toSidebarLabel(folderName, isRoot = false) {
  if (!isRoot) {
    return folderName;
  }
  return folderName.replace(/^\d{2}_/, '');
}

const rootItems = ROOT_ORDER.filter((dirName) => fs.existsSync(path.join(DOCS_ROOT, dirName))).map(
  (dirName) => ({
    type: 'category',
    label: toSidebarLabel(dirName, true),
    collapsed: true,
    items: [{type: 'autogenerated', dirName}],
  }),
);

/** @type {import('@docusaurus/plugin-content-docs').SidebarsConfig} */
const sidebars = {
  docs: [
    'intro',
    {
      type: 'category',
      label: 'K-DOCS',
      collapsed: false,
      items: rootItems,
    },
  ],
};

module.exports = sidebars;
